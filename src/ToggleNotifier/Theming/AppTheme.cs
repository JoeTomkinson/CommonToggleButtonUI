namespace ToggleNotifier.Theming;

/// <summary>
/// Represents the available application themes.
/// </summary>
public enum AppTheme
{
    /// <summary>
    /// Follow the Windows system theme.
    /// </summary>
    System = 0,

    /// <summary>
    /// Light theme with light backgrounds and dark text.
    /// </summary>
    Light = 1,

    /// <summary>
    /// Dark theme with dark backgrounds and light text.
    /// </summary>
    Dark = 2
}
